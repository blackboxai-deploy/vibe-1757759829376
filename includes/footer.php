    <script>
        // Animate Statistics Counter
        function animateCounter(element, target) {
            let current = 0;
            const increment = target / 100;
            const timer = setInterval(() => {
                current += increment;
                if (current >= target) {
                    element.textContent = target.toLocaleString() + '+';
                    clearInterval(timer);
                } else {
                    element.textContent = Math.floor(current).toLocaleString() + '+';
                }
            }, 20);
        }
        
        // Floating Hearts Effect
        function createFloatingHeart() {
            const heart = document.createElement('div');
            heart.className = 'absolute text-2xl pointer-events-none animate-float';
            heart.innerHTML = Math.random() > 0.5 ? '💕' : '💖';
            heart.style.left = Math.random() * 100 + 'px';
            heart.style.bottom = '0px';
            heart.style.opacity = '0.8';
            
            document.getElementById('floating-hearts').appendChild(heart);
            
            // Remove heart after animation
            setTimeout(() => {
                heart.remove();
            }, 4000);
        }
        
        // Loading functions
        function showLoading() {
            document.getElementById('loading-overlay').classList.remove('hidden');
        }
        
        function hideLoading() {
            document.getElementById('loading-overlay').classList.add('hidden');
        }
        
        // Initialize on page load
        document.addEventListener('DOMContentLoaded', function() {
            // Animate statistics
            const userCount = document.getElementById('user-count');
            const matchCount = document.getElementById('match-count');
            const messageCount = document.getElementById('message-count');
            const storyCount = document.getElementById('story-count');
            
            if (userCount) animateCounter(userCount, 2500000);
            if (matchCount) animateCounter(matchCount, 500000);
            if (messageCount) animateCounter(messageCount, 10000000);
            if (storyCount) animateCounter(storyCount, 50000);
            
            // Create floating hearts periodically
            setInterval(createFloatingHeart, 5000);
            
            // Add romantic glow effect on hover
            const cards = document.querySelectorAll('.card-dating, .swipe-card');
            cards.forEach(card => {
                card.addEventListener('mouseenter', function() {
                    this.classList.add('romantic-glow');
                });
                card.addEventListener('mouseleave', function() {
                    this.classList.remove('romantic-glow');
                });
            });
        });
    </script>
